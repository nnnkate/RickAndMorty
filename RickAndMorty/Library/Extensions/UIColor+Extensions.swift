import UIKit

// MARK: - Custom colors

extension UIColor {
    class var customDarkGrayColor: UIColor {
        UIColor(red: 0.10, green: 0.10, blue: 0.10, alpha: 1)
    }
    
    class var customGrayColor: UIColor {
        UIColor(red: 0.30, green: 0.30, blue: 0.30, alpha: 1)
    }
}

